    private void btnXacNhanActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_btnXacNhanActionPerformed
        // Lấy tên nhân viên hiện tại từ server
        Request reqNV = new Request(CommandType.GET_NHAN_VIEN_BY_MA,
                GiaoDienChinhGUI.getTk().getMaNV());
        Response resNV = SocketClient.getInstance().sendRequest(reqNV);
        if (resNV.isSuccess() && resNV.getData() != null) {
            jTextField3.setText(resNV.getData().toString());
        }

        DefaultTableModel dtmCTHD = (DefaultTableModel) tblCTHD.getModel();
        int soLuongChon = 0;
        for (int i = 0; i < dtmCTHD.getRowCount(); i++) {
            if (dtmCTHD.getValueAt(i, 8) == Boolean.TRUE
                    && Integer.parseInt(dtmCTHD.getValueAt(i, 3).toString()) > 0) {
                soLuongChon++;
            }
        }
        jLabel6.setText("Số lượng đã chọn: " + soLuongChon);

        DefaultTableModel dtmTraHang = (DefaultTableModel) tblTraHang.getModel();
        dtmTraHang.setRowCount(0);

        int count = 0;
        int sttTra = 1;
        for (int i = 0; i < dtmCTHD.getRowCount(); i++) {
            if (dtmCTHD.getValueAt(i, 8) == Boolean.TRUE
                    && Integer.parseInt(dtmCTHD.getValueAt(i, 3).toString()) > 0) {

                // Đọc thẳng từ tblCTHD – không cần gọi server lại
                // tblCTHD: STT | Mã CTHD | Tên SP | SL | ĐVT | Đơn giá | Giảm giá | Thành tiền | Chọn
                String maCTHD    = dtmCTHD.getValueAt(i, 1).toString();
                String tenSP     = dtmCTHD.getValueAt(i, 2).toString();
                int    soLuong   = Integer.parseInt(dtmCTHD.getValueAt(i, 3).toString());
                Object donGia    = dtmCTHD.getValueAt(i, 5);
                Object giamGia   = dtmCTHD.getValueAt(i, 6);
                Object thanhTien = dtmCTHD.getValueAt(i, 7);

                // Build row cho tblTraHang
                // tblTraHang: STT | Tên SP | SL | Đơn giá | Giảm giá | Thành tiền |
                //             Lý do | Nguyên vẹn | % hoàn | Tiền hoàn | Mã CTHD
                Object[] rowData = new Object[]{
                        sttTra++,
                        tenSP,
                        soLuong,
                        donGia,
                        giamGia,
                        thanhTien,
                        "Hàng lỗi do nhà sản xuất",   // lý do mặc định
                        Boolean.TRUE,                  // SP nguyên vẹn mặc định
                        "100%",                        // % hoàn trả mặc định
                        thanhTien,                     // tiền hoàn ban đầu = thành tiền
                        maCTHD
                };
                dtmTraHang.addRow(rowData);
                taoLyDo();
                count++;
            }
        }

        capNhatTongThanhTien();
        if (count > 0) {
            jTabbedPane1.setSelectedIndex(1);
        } else {
            JOptionPane.showMessageDialog(null, "Vui lòng chọn sản phẩm muốn trả");
        }
    }// GEN-LAST:event_btnXacNhanActionPerformed

