    private String dinhDangTien(double tien) {
        return new java.text.DecimalFormat("#,###").format(tien) + " VNĐ";
    }

    public double boDinhDangTien(String formatted) {
        String cleaned = formatted.replace("VNĐ", "").replace(",", "").trim();
        if (cleaned.isEmpty()) return 0;
        return Double.parseDouble(cleaned);
    }

    /** Sinh mã phiếu trả hàng theo format PTH-DDMMYY-XXXX */
    private String phatSinhMaPhieuTraHang(int soThuTu) {
        java.time.LocalDate t = java.time.LocalDate.now();
        String ddMMyy = String.format("%02d%02d%02d",
                t.getDayOfMonth(), t.getMonthValue(), t.getYear() % 100);
        return String.format("PTH-%s-%04d", ddMMyy, soThuTu);
    }

