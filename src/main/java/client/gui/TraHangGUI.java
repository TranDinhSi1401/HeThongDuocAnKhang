    private void mapKeyToFocus(String key, javax.swing.JComponent component) {
        javax.swing.InputMap  im = component.getInputMap(javax.swing.JComponent.WHEN_IN_FOCUSED_WINDOW);
        javax.swing.ActionMap am = component.getActionMap();
        im.put(javax.swing.KeyStroke.getKeyStroke(key), "focus_" + key);
        am.put("focus_" + key, new javax.swing.AbstractAction() {
            @Override public void actionPerformed(java.awt.event.ActionEvent e) {
                component.requestFocus();
                if (component instanceof javax.swing.JTextField tf) tf.selectAll();
            }
        });
    }

    private void mapKeyToClickButton(String key, javax.swing.AbstractButton button) {
        javax.swing.InputMap  im = button.getInputMap(javax.swing.JComponent.WHEN_IN_FOCUSED_WINDOW);
        javax.swing.ActionMap am = button.getActionMap();
        im.put(javax.swing.KeyStroke.getKeyStroke(key), "click_" + key);
        am.put("click_" + key, new javax.swing.AbstractAction() {
            @Override public void actionPerformed(java.awt.event.ActionEvent e) { button.doClick(); }
        });
    }
