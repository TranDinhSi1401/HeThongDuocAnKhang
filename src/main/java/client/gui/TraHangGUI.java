    private void tblTraHangPropertyChange(java.beans.PropertyChangeEvent evt) {// GEN-FIRST:event_tblTraHangPropertyChange
        DefaultTableModel dtm = (DefaultTableModel) tblTraHang.getModel();
        int selectRow = tblTraHang.getSelectedRow();
        if (selectRow < 0) return;

        boolean nguyenVen = (boolean) dtm.getValueAt(selectRow, 7);
        String lyDoStr = dtm.getValueAt(selectRow, 6).toString();
        double thanhTienGoc = boDinhDangTien(dtm.getValueAt(selectRow, 5).toString());

        // Tính % hoàn trả theo quy tắc nghiệp vụ (client-side)
        double phanTram;
        String hienThiPhanTram;
        if (nguyenVen) {
            switch (lyDoStr) {
                case "Hàng lỗi do nhà sản xuất" -> { phanTram = 1.0;  hienThiPhanTram = "100%"; }
                case "Dị ứng/mẫn cảm"          -> { phanTram = 0.5;  hienThiPhanTram = "50%";  }
                default                         -> { phanTram = 0.7;  hienThiPhanTram = "70%";  }
            }
        } else {
            switch (lyDoStr) {
                case "Hàng lỗi do nhà sản xuất" -> { phanTram = 0.8;  hienThiPhanTram = "80%";  }
                case "Dị ứng/mẫn cảm"          -> { phanTram = 0.3;  hienThiPhanTram = "30%";  }
                default                         -> { phanTram = 0.5;  hienThiPhanTram = "50%";  }
            }
        }

        double tienHoanTra = thanhTienGoc * phanTram;
        dtm.setValueAt(hienThiPhanTram, selectRow, 8);
        dtm.setValueAt(dinhDangTien(tienHoanTra), selectRow, 9);
        capNhatTongThanhTien();
    }



