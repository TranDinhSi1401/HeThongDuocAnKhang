    /**
     * Cập nhật tổng thành tiền hoàn trả và điểm trừ.
     */
    private void capNhatTongThanhTien() {
        DefaultTableModel dtm = (DefaultTableModel) tblTraHang.getModel();
        double tongThanhTien = 0;
        for (int i = 0; i < dtm.getRowCount(); i++) {
            tongThanhTien += boDinhDangTien(dtm.getValueAt(i, 9).toString());
        }
        txtTongThanhTien.setText(dinhDangTien(tongThanhTien));

        String diemStr = txtDiemTichLuy.getText();
        if (diemStr != null && !diemStr.isEmpty() && !diemStr.equals("0")) {
            txtDiemTru.setText(String.valueOf((int) (tongThanhTien / 1000)));
        } else {
            txtDiemTru.setText("0");
        }
    }

