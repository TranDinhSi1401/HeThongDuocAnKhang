    private void btnTaoPhieuActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_btnTaoPhieuActionPerformed
        int chon = JOptionPane.showConfirmDialog(null, "Xác nhận tạo phiếu trả?",
                "Xác nhận", JOptionPane.YES_NO_OPTION);
        if (chon != JOptionPane.YES_OPTION) return;

        DefaultTableModel dtm = (DefaultTableModel) tblTraHang.getModel();
        if (dtm.getRowCount() == 0) {
            JOptionPane.showMessageDialog(null, "Chưa có sản phẩm nào trong phiếu trả!");
            return;
        }

        // ── 1. Tạo phiếu trả hàng (header) — server tự sinh mã ──
        PhieuTraHangDTO headerDTO = PhieuTraHangDTO.builder()
                .maNV(GiaoDienChinhGUI.getTk().getMaNV())
                .maHoaDon(txtMaHoaDonTrongPhieuTraHang.getText())
                .ngayLapPhieuTraHang(java.time.LocalDateTime.now())
                .tongTienHoaTra(boDinhDangTien(txtTongThanhTien.getText()))
                .build();

        Response resHeader = SocketClient.getInstance().sendRequest(
                new Request(CommandType.ADD_PHIEU_TRA_HANG, headerDTO));
        if (!resHeader.isSuccess()) {
            JOptionPane.showMessageDialog(null, "Lỗi khi tạo phiếu: " + resHeader.getMessage());
            return;
        }
        // Server trả về mã PTH thực tế đã lưu
        String maPTH = resHeader.getData() != null
                ? resHeader.getData().toString()
                : txtMaPhieuTraHang.getText().trim();

        // ── 2. Lưu từng chi tiết phiếu trả ─────────────────────
        for (int i = 0; i < dtm.getRowCount(); i++) {
            // tblTraHang: STT | Tên SP | SL | Đơn giá | Giảm giá | Thành tiền |
            //             Lý do | Nguyên vẹn | % hoàn | Tiền hoàn | Mã CTHD
            String  maCTHD     = dtm.getValueAt(i, 10).toString();
            int     soLuong    = ((Number) dtm.getValueAt(i, 2)).intValue();
            String  lyDo       = dtm.getValueAt(i, 6).toString();
            boolean nguyenVen  = (Boolean) dtm.getValueAt(i, 7);
            String  phanTram   = dtm.getValueAt(i, 8).toString().replace("%", "").trim();
            double  tienHoan   = boDinhDangTien(dtm.getValueAt(i, 9).toString());

            ChiTietPhieuTraHangDTO ctDTO = ChiTietPhieuTraHangDTO.builder()
                    .maPhieuTraHang(maPTH)
                    .maChiTietHoaDon(maCTHD)
                    .soLuong(soLuong)
                    .giaTriHoanTra(phanTram)   // dạng số 100, 80, 50, 30
                    .thanhTienHoanTra(tienHoan)
                    .truongHopDoiTra(mapLyDoSangEnum(lyDo))         // String enum name
                    .tinhTrangSanPham(nguyenVen ? "HANG_NGUYEN_VEN" : "HANG_KHONG_NGUYEN_VEN")
                    .build();

            Response resCT = SocketClient.getInstance().sendRequest(
                    new Request(CommandType.ADD_CHI_TIET_PHIEU_TRA_HANG, ctDTO));
            if (!resCT.isSuccess()) {
                JOptionPane.showMessageDialog(null,
                        "Lỗi khi lưu chi tiết phiếu trả: " + resCT.getMessage());
                return;
            }
        }

        jTabbedPane1.setSelectedIndex(0);
        xoaRongTatCa();
        JOptionPane.showMessageDialog(null, "Tạo phiếu trả hàng thành công!");
    }// GEN-LAST:event_btnTaoPhieuActionPerformed

