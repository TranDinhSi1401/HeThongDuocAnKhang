    /**
     * Tìm hóa đơn để trả hàng – dùng nhiều request riêng thay vì một DTO tổng hợp.
     *
     * Các command dùng:
     *  - GET_HOA_DON_BY_MA       → HoaDonDTO
     *  - GET_SO_PTH              → int   (số phiếu trả đã tạo cho HD này)
     *  - GET_TONG_TIEN_CAC_PTH   → double
     *  - GET_SO_PTH_CUOI         → String (mã PTH mới sinh)
     *  - GET_KHACH_HANG_BY_MA    → KhachHangDTO  (chỉ gọi nếu HD có maKH)
     *  - GET_CTHD_BY_MA_HD       → ArrayList<ChiTietHoaDonDTO>
     */
    private void timHoaDonDeTraHang() {
        String maHoaDon = txtNhapMaHoaDon.getText().trim();
        if (maHoaDon.isEmpty() || maHoaDon.equals("[F3]")) return;
        resetInfoPanels();

        // ── 1. Lấy hóa đơn ──────────────────────────────────────
        Response resHD = SocketClient.getInstance().sendRequest(
                new Request(CommandType.GET_HOA_DON_BY_MA, maHoaDon));
        if (!resHD.isSuccess()) {
            JOptionPane.showMessageDialog(null, resHD.getMessage());
            return;
        }
        HoaDonDTO hd = (HoaDonDTO) resHD.getData();

        // Chặn HD quá 30 ngày
        long soNgay = ChronoUnit.DAYS.between(
                hd.getNgayLapHoaDon().toLocalDate(), LocalDate.now());
        if (soNgay > 30) {
            JOptionPane.showMessageDialog(null,
                    "Hóa đơn đã lập hơn 30 ngày, không thể trả hàng!");
            return;
        }

        // ── 2. Số phiếu trả hàng đã có của HD này ───────────────
        Response resSoPTH = SocketClient.getInstance().sendRequest(
                new Request(CommandType.GET_SO_PTH, maHoaDon));
        int soPTH = (resSoPTH.isSuccess() && resSoPTH.getData() != null)
                ? ((Number) resSoPTH.getData()).intValue() : 0;

        // ── 3. Tổng tiền các phiếu trả ──────────────────────────
        Response resTong = SocketClient.getInstance().sendRequest(
                new Request(CommandType.GET_TONG_TIEN_CAC_PTH, maHoaDon));
        double tongTienPTH = (resTong.isSuccess() && resTong.getData() != null)
                ? ((Number) resTong.getData()).doubleValue() : 0;

        // ── 4. Sinh mã phiếu trả hàng mới ───────────────────────
        Response resSoCuoi = SocketClient.getInstance().sendRequest(
                new Request(CommandType.GET_SO_PTH_CUOI, null));
        int soCuoi = (resSoCuoi.isSuccess() && resSoCuoi.getData() != null)
                ? ((Number) resSoCuoi.getData()).intValue() : 0;
        String maPTHMoi = phatSinhMaPhieuTraHang(soCuoi + 1);

        // ── 5. Thông tin khách hàng (nếu có) ────────────────────
        String tenKH = "Khách lẻ";
        int diem = 0;
        if (hd.getMaKH() != null && !hd.getMaKH().isBlank()) {
            Response resKH = SocketClient.getInstance().sendRequest(
                    new Request(CommandType.GET_KHACH_HANG_BY_MA, hd.getMaKH()));
            if (resKH.isSuccess() && resKH.getData() instanceof KhachHangDTO kh) {
                tenKH = kh.getHoTenDem() + " " + kh.getTen();
                diem  = kh.getDiemTichLuy();
            }
        }

        // ── 6. Chi tiết hóa đơn → điền tblCTHD ─────────────────
        Response resCTHD = SocketClient.getInstance().sendRequest(
                new Request(CommandType.GET_CTHD_BY_MA_HD, maHoaDon));
        if (!resCTHD.isSuccess()) {
            JOptionPane.showMessageDialog(null, resCTHD.getMessage());
            return;
        }

        // ── Cập nhật 3 card thông tin ────────────────────────────
        lblSoLanTra.setText(String.valueOf(soPTH));
        lblSoNgayHD.setText(String.valueOf(soNgay));
        lblTongTienTra.setText(dinhDangTien(tongTienPTH));

        txtTenKhachHang.setText(tenKH);
        txtDiemTichLuy.setText(String.valueOf(diem));

        txtMaHoaDonTrongPhieuTraHang.setText(maHoaDon);
        jTextField5.setText(LocalDate.now().format(DateTimeFormatter.ofPattern("dd/MM/yyyy")));
        txtMaPhieuTraHang.setText(maPTHMoi);

        // ── 6.5. Tính số lượng đã trả của từng CTHD trong các phiếu trả trước ──
        java.util.Map<String, Integer> daTraMap = tinhSoLuongDaTra(maHoaDon);

        // ── Điền bảng chi tiết hóa đơn ──────────────────────────
        // tblCTHD columns: STT | Mã CTHD | Tên SP | Số lượng | ĐVT | Đơn giá | Giảm giá | Thành tiền | Chọn
        DefaultTableModel bangCTHD = (DefaultTableModel) tblCTHD.getModel();
        bangCTHD.setRowCount(0);
        @SuppressWarnings("unchecked")
        ArrayList<ChiTietHoaDonDTO> dsCTHD = (ArrayList<ChiTietHoaDonDTO>) resCTHD.getData();
        int stt = 1;
        for (ChiTietHoaDonDTO ct : dsCTHD) {
            int daTra  = daTraMap.getOrDefault(ct.getMaChiTietHoaDon(), 0);
            int conLai = ct.getSoLuong() - daTra;
            if (conLai <= 0) continue;   // bỏ qua sản phẩm đã trả hết

            // Thành tiền hiển thị tính lại theo số lượng còn có thể trả
            double thanhTienConLai = ct.getDonGia() * conLai * (1 - ct.getGiamGia() / 100.0);

            bangCTHD.addRow(new Object[]{
                    stt++,
                    ct.getMaChiTietHoaDon(),
                    "ct.getTenSanPham()",
                    conLai,                              // số lượng còn được phép trả
                    "ct.getTenDonViTinh()",
                    dinhDangTien(ct.getDonGia()),
                    ct.getGiamGia(),
                    dinhDangTien(thanhTienConLai),
                    Boolean.FALSE                        // cột Chọn
            });
        }
        txtNhapMaHoaDon.setText("");
    }

    /**
     * Gom số lượng đã trả của từng CTHD trong tất cả các phiếu trả trước đó.
     * Key = maChiTietHoaDon, Value = tổng số lượng đã trả.
     */
    private java.util.Map<String, Integer> tinhSoLuongDaTra(String maHD) {
        java.util.Map<String, Integer> result = new java.util.HashMap<>();

        // 1. Lấy danh sách phiếu trả của HD này
        Response resPTH = SocketClient.getInstance().sendRequest(
                new Request(CommandType.GET_PTH_BY_MA_HD, maHD));
        if (!resPTH.isSuccess() || !(resPTH.getData() instanceof ArrayList)) return result;
        @SuppressWarnings("unchecked")
        ArrayList<PhieuTraHangDTO> dsPTH = (ArrayList<PhieuTraHangDTO>) resPTH.getData();

        // 2. Với mỗi phiếu, lấy chi tiết và cộng dồn số lượng
        for (PhieuTraHangDTO pth : dsPTH) {
            Response resCT = SocketClient.getInstance().sendRequest(
                    new Request(CommandType.GET_CTPTH_BY_MA_PTH, pth.getMaPhieuTraHang()));
            if (!resCT.isSuccess() || !(resCT.getData() instanceof ArrayList)) continue;

            @SuppressWarnings("unchecked")
            ArrayList<ChiTietPhieuTraHangDTO> dsCT =
                    (ArrayList<ChiTietPhieuTraHangDTO>) resCT.getData();
            for (ChiTietPhieuTraHangDTO ct : dsCT) {
                result.merge(ct.getMaChiTietHoaDon(), ct.getSoLuong(), Integer::sum);
            }
        }
        return result;
    }

